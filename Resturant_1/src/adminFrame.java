    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try {
            Connecting2DB conn = new Connecting2DB();
            String query = "select sup_id from supplieres where sup_id = " + Integer.parseInt(itemSupTextField.getText());
            if (conn.retriveOne(query, "sup_id") != null) {
                int supplier_id = Integer.parseInt(conn.retriveOne(query, "sup_id"));
                query = "insert into item values('" + ItemNameTextField.getText() + "', " + Double.parseDouble(itemPriceTextField.getText()) + ", " + Double.parseDouble(itemCalTextField.getText()) + ", " + supplier_id + ")";
                conn.insert_into_table(query);
                JOptionPane.showMessageDialog(null, "Item has been added!");
                new adminFrame().setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Sup_id is not found");
            }
        } catch (NumberFormatException NumberFormatException) {
            JOptionPane.showMessageDialog(null, "Enter Numbers Only for ID, Cal and Price");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

