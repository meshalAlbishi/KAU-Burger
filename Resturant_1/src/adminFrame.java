    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        Connecting2DB conn = new Connecting2DB();
        try {
            String job_type = null;
            if (isDriver) {
                job_type = "Driver";
            } else if (isCasher) {
                job_type = "Cashier";
            }
            if (job_type != null) {
                String query = "insert into employee values (" + Integer.parseInt(SSNTextField.getText())
                        + ",'" + phoneTextField.getText() + "','" + addressTextField.getText() + "'," + "to_date('"
                        + DobTextField.getText() + "'),'" + fNameTextField.getText() + "','" + lNameTextField11.getText()
                        + "','" + job_type + "',";
                if (superIDTextField.getText().equals("")) {
                    query += "null";
                } else {
                    query += Integer.parseInt(superIDTextField.getText());
                }
                query += ")";
                conn.insert_into_table(query);

                if (isDriver) {
                    query = "insert into driving_license values(" + Integer.parseInt(LiceneNumField.getText()) + ",to_date('" + liceneExpField.getText() + "'),'" + bloodTypeField.getText() + "')";
                    conn.insert_into_table(query);
                    query = "insert into driver values(" + nextDeiverID + "," + Integer.parseInt(SSNTextField.getText()) + "," + Integer.parseInt(LiceneNumField.getText()) + ")";
                    conn.insert_into_table(query);
                } else if (isCasher) {
                    query = "insert into cashir values(" + nextCashierID + ",'" + usernameField1.getText() + "'," + "'0'," + Integer.parseInt(SSNTextField.getText()) + ")";
                    conn.insert_into_table(query);
                }
                JOptionPane.showMessageDialog(null, "Employee has been added!");
                nextCashierID++;
                nextDeiverID++;
                
                new adminFrame().setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Chose JOB TYPE!");
            }
        } catch (NumberFormatException NumberFormatException) {
            JOptionPane.showMessageDialog(null, NumberFormatException.getMessage());
        }

    }//GEN-LAST:event_jButton5ActionPerformed

