    private void CalculateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CalculateActionPerformed
        double total = 0.0;
        Connecting2DB conn = new Connecting2DB();
        if (item.size() > 0) {
            for (int i = 0; i < item.size(); i++) {
                String q = "select Price from item where name = '" + item.get(i) + "'";
                double price = Double.parseDouble(conn.retriveOne(q, "Price"));
                total += price * itemQty.get(i);
            }
            totalField.setText(total + "");
        } else {
            JOptionPane.showMessageDialog(null, "You didn't ADD any ITEM!");
        }
    }//GEN-LAST:event_CalculateActionPerformed

