    private void printReciptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_printReciptActionPerformed
        if (changeField.getText().isEmpty() || changeField.getText().equals(" ") || changeField.getText() == null) {
            JOptionPane.showMessageDialog(null, "You didn't pay yet!", null, JOptionPane.ERROR_MESSAGE);
        } else {
            double change = Double.parseDouble(changeField.getText());
            boolean orderIsConfirmed = false;
            if (change <= 0) {
                Connecting2DB conn = new Connecting2DB();
                String Type = (String) orderType.getSelectedItem();
                String q = "select sysdate from dual";
                String date = conn.retriveOne(q, "sysdate");
                int index = date.indexOf(" ");
                date = date.substring(0, index);
                double total = Double.parseDouble(totalField.getText());
                q = "insert into bill values( " + nextBillNumber + "," + total + ")";
                conn.insert_into_table(q);
                q = "insert into orders values( " + nextOrderID + " , '" + Type + "', to_date('" + date + "','yyyy/mm/dd'), " + cashierID + " ," + nextBillNumber + ")";
                conn.insert_into_table(q);
                if (isDelivery) {
                    try {
                        if (!customerIDFiled.getText().equals("")) {
                            int carID = Integer.parseInt((String) carsBox.getSelectedItem());
                            int driverID = Integer.parseInt((String) driversBox.getSelectedItem());
                            int customerID = Integer.parseInt((String) customerIDFiled.getText());
                            if (customerID > 0) {
                                String name = nameFiled.getText();
                                if (name == null) {
                                    JOptionPane.showMessageDialog(null, "Customer not Found!");
                                } else {
                                    q = "insert into delivery_order values (" + nextOrderID + "," + carID + "," + customerID + "," + driverID + ")";
                                    conn.insert_into_table(q);
                                    orderIsConfirmed = true;
                                }
                            } else {
                                JOptionPane.showMessageDialog(null, "Customer ID in delivery order cannot be 0");
                            }
                        } else {
                            JOptionPane.showMessageDialog(null, "Please Enter CUSTOMER ID!");
                        }
                    } catch (NumberFormatException NumberFormatException) {
                        JOptionPane.showMessageDialog(null, "Enter Numbers Only");
                    }

                } else {
                    int customerID = Integer.parseInt((String) customerIDFiled.getText());
                    q = "select name from customer where customer_id = " + customerID;
                    String name = conn.retriveOne(q, "name");
                    if (name == null) {
                        q = "insert into customer values(0,'default',null,null)";
                        conn.insert_into_table(q);
                    }
                    q = "insert into pickup_order values(" + nextOrderID + ", " + customerID + ")";
                    conn.insert_into_table(q);
                    orderIsConfirmed = true;
                }
                if (orderIsConfirmed) {
                    nextBillNumber++;
                    nextOrderID++;
                    new cashierFrame(cashierID).setVisible(true);
                    this.dispose();
                }
            } else {
                JOptionPane.showMessageDialog(null, "change Must be <= 0", null, JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_printReciptActionPerformed

    private void paidBouttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_paidBouttonActionPerformed
        if (totalField.getText().isEmpty() || totalField.getText().equals(" ") || totalField.getText() == null) {
            JOptionPane.showMessageDialog(null, "Total Must be > 0", null, JOptionPane.ERROR_MESSAGE);
        } else {
            double paid = 0;
            try {
                paid = Double.parseDouble(JOptionPane.showInputDialog("How much?"));
                paidFiled.setText(paid + "");
                double total = Double.parseDouble(totalField.getText());
                double change = total - paid;
                changeField.setText(Double.toString(change));
            } catch (NumberFormatException NumberFormatException) {
                JOptionPane.showMessageDialog(null, "Enter Numbers Only");
            }
        }
    }//GEN-LAST:event_paidBouttonActionPerformed

