    private void orderTypeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_orderTypeActionPerformed
        if (orderType.getSelectedItem().equals("Pickup")) {
            customerIDFiled.setEditable(false);
            customerIDFiled.setText("0");
            carsBox.setEnabled(false);
            driversBox.setEnabled(false);
            isPickUp = true;
            isDelivery = false;
            Connecting2DB conn = new Connecting2DB();
            int customerID = Integer.parseInt((String) customerIDFiled.getText());
            String q = "select name from customer where customer_id = " + customerID;
            String name = conn.retriveOne(q, "name");
            if (name == null) {
                q = "insert into customer values(0,'default',null,null)";
                conn.insert_into_table(q);
            }
        } else {
            customerIDFiled.setEditable(true);
            carsBox.setEnabled(true);
            driversBox.setEnabled(true);
            isPickUp = false;
            isDelivery = true;
        }
    }//GEN-LAST:event_orderTypeActionPerformed

